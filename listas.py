#Declaração
nomes = ['João', 'Maria', 'Ana']
print(nomes)

#Acessando um elemento diretamente
nomes = ['João', 'Maria', 'Ana']
x =nomes[2]
print(x)

#Tamanho da lista
nomes = ['João', 'Maria', 'Ana']
x =len(nomes)
print(x)

#Iterando sobre a lista
nomes = ['João', 'Maria', 'Ana']
for x in nomes:
    print(x)

#Adicionar ao final
nomes = ['João', 'Maria', 'Ana']
print(nomes)
nomes.append('José')
print(nomes)

#Remover ao final
nomes = ['João', 'Maria', 'Ana']
print(nomes)
nomes.remove('João')
print(nomes)

